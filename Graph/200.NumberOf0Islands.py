def numberOfIslands(): 
    n = len(grid)
    m = len(grid[0])
    result = 0
    visited = set()
    directions = [[0, 1], [0, -1], [1, 0], [-1, 0]]

    def dfs(r, c):
        if (r not in range(n) or c not in range(m) or grid[r][c] == "0" or (r, c) in visited):
            return

        visited.add((r, c))
        
        for dr, dc in directions:
            dfs(r + dr, c + dc)

    for row in range(n): 
        for col in range(m): 
            if (row, col) not in visited and grid[row][col] == "1": 
                dfs(row, col)
                result += 1

    return result


